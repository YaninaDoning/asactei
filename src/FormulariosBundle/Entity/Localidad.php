<?php

namespace FormulariosBundle\Entity;

class Localidad
{
    const ID_SANTA_FE = 304;
    
    private $id;
    private $nombre;
    private $departamento;
    private $departamento_vid;
    private $localidad_vid;
    private $region;

    /**
     * Get id
     *
     * @return integer 
     */
    public function getId()
    {
        return $this->id;
    }

    /**
     * Set nombre
     *
     * @param string $nombre
     * @return Localidad
     */
    public function setNombre($nombre)
    {
        $this->nombre = $nombre;

        return $this;
    }

    /**
     * Get nombre
     *
     * @return string 
     */
    public function getNombre()
    {
        return $this->nombre;
    }

    /**
     * Set departamento
     *
     * @param string $departamento
     * @return Localidad
     */
    public function setDepartamento($departamento)
    {
        $this->departamento = $departamento;

        return $this;
    }

    /**
     * Get departamento
     *
     * @return string 
     */
    public function getDepartamento()
    {
        return $this->departamento;
    }

    /**
     * Set departamento_vid
     *
     * @param integer $departamentoVid
     * @return Localidad
     */
    public function setDepartamentoVid($departamentoVid)
    {
        $this->departamento_vid = $departamentoVid;

        return $this;
    }

    /**
     * Get departamento_vid
     *
     * @return integer 
     */
    public function getDepartamentoVid()
    {
        return $this->departamento_vid;
    }

    /**
     * Set localidad_vid
     *
     * @param integer $localidadVid
     * @return Localidad
     */
    public function setLocalidadVid($localidadVid)
    {
        $this->localidad_vid = $localidadVid;

        return $this;
    }

    /**
     * Get localidad_vid
     *
     * @return integer 
     */
    public function getLocalidadVid()
    {
        return $this->localidad_vid;
    }

    /**
     * Set region
     *
     * @param integer $region
     * @return Localidad
     */
    public function setRegion($region)
    {
        $this->region = $region;

        return $this;
    }

    /**
     * Get region
     *
     * @return integer 
     */
    public function getRegion()
    {
        return $this->region;
    }
}
